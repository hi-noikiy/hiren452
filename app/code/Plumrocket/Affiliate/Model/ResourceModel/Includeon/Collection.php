namespace Plumrocket\Affiliate\Model\ResourceModel\Includeon;

class Collection extends \Magento\Framework\Model\ResourceModel\Db\Collection\AbstractCollection
{
    /**
     * Constructor
     * Configures collection
     *
     * @return void
     */
    protected function _construct()
    {
        parent::_construct();
        $this->_init('Plumrocket\Affiliate\Model\Includeon', 'Plumrocket\Affiliate\Model\ResourceModel\Includeon');
    }

    /**
     * Add enabled filter to collection
     * @return $this 
     */
    public function addEnabledFilter()
    {
        return $this->addFieldToFilter('status', 1);
    }
}
